package first;

public class PrimitiveDataTypeExam {
	public static void main (String[] args) {
		boolean isFun = true;
		
		System.out.println(isFun);
		
		char c = 'f';
//		char 는 ''로 표현
		int x = 59;
		
		long bing = 345345345L;
//		long data type 뒤에 소문자 l,대문자 L
		
		float f = 32.4f;
//		float data type 뒤에 소문자 f, 대문자 F
		
		double d = 345345.3;
	}
}