package first;
public class OperatorExam4 {
	
	public static void main (String[] agrs) {
		int i = 10;
		int j = 10;
		
		System.out.println (i == j);
		System.out.println (i != j);
		System.out.println (i < j);
		System.out.println (i <= j);
		System.out.println (i > j);
		System.out.println (i >= j);
		
		i += 10; //i = i + 10 복합대입연산자
		System.out.println (i);
		System.out.println (i -= 5);
	
	}
}
